function renderSvelteFiles(): CreateTemplateFile[] {
  return [
    {
      path: 'src/types.ts',
      content: withFinalNewline(`export type HomeData = {
  message: string;
};
`),
    },
    {
      path: 'src/svelte.d.ts',
      content: withFinalNewline(`declare module '*.svelte' {
  import type { Component } from 'svelte';

  const component: Component<Record<string, unknown>>;
  export default component;
}
`),
    },
    {
      path: 'src/App.svelte',
      content: withFinalNewline(`<script lang="ts">
  let { message }: { message: string } = $props();
</script>

<main>
  <h1>FaceTheory Svelte starter</h1>
  <p>{message}</p>
</main>
`),
    },
    {
      path: 'src/face-app.ts',
      content: withFinalNewline(`import { createFaceApp } from '@theory-cloud/facetheory';
import { createSvelteFace } from '@theory-cloud/facetheory/svelte';

import App from './App.svelte';
import type { HomeData } from './types.js';

const CLIENT_BOOTSTRAP_MODULE = '${CLIENT_BOOTSTRAP_MODULE}';

export const app = createFaceApp({
  faces: [
    createSvelteFace<HomeData, HomeData>({
      route: '/',
      mode: 'ssr',
      load: async () => ({ message: 'Hello from a FaceTheory Svelte Face.' }),
      render: (_ctx, data) => ({
        component: App,
        props: { message: data.message },
      }),
      renderOptions: (_ctx, data) => ({
        headTags: [
          { type: 'title', text: 'FaceTheory Svelte starter' },
          { type: 'meta', attrs: { name: 'viewport', content: 'width=device-width,initial-scale=1' } },
        ],
        hydration: {
          data,
          bootstrapModule: CLIENT_BOOTSTRAP_MODULE,
        },
      }),
    }),
  ],
});
`),
    },
    {
      path: 'src/client.ts',
      content: withFinalNewline(`import { loadFaceHydrationData } from '@theory-cloud/facetheory/client';
import { hydrate } from 'svelte';

import App from './App.svelte';
import type { HomeData } from './types.js';

const data = (await loadFaceHydrationData<HomeData>()) ?? {
  message: 'Hello from the client fallback.',
};
const root = document.getElementById('root');

if (!root) {
  throw new Error('FaceTheory Svelte starter could not find #root to hydrate');
}

hydrate(App, {
  target: root,
  props: { message: data.message },
});
`),
    },
  ];
}

export function renderCreateTemplate(ctx: CreateTemplateContext): CreateTemplateFile[] {
  const adapterFiles =
    ctx.adapter === 'react'
      ? renderReactFiles()
      : ctx.adapter === 'vue'
        ? renderVueFiles()
        : renderSvelteFiles();

  return [
    { path: 'package.json', content: renderPackageJson(ctx) },
    { path: 'tsconfig.json', content: renderTsconfig(ctx) },
    { path: 'README.md', content: renderReadme(ctx) },
    { path: 'vite.config.ts', content: renderViteConfig(ctx) },
    { path: 'src/handler.ts', content: renderHandler() },
    { path: 'infra/app.ts', content: renderInfraApp() },
    { path: 'infra/stack.ts', content: renderInfraStack() },
    ...adapterFiles,
  ];
}
